/**
 * 根据分类获取字符图案
 */
export function getPatternsByCategory(category: CharacterPattern['category']): CharacterPattern[] {
  return characterPatterns.filter((p) => p.category === category);
}

/**
 * 根据ID获取字符图案
 */
export function getPatternById(id: string): CharacterPattern | undefined {
  return characterPatterns.find((p) => p.id === id);
}

/**
 * 将字符图案转换为布尔数组（用于兼容现有代码）
 */
export function gridToBoolean(grid: number[][]): boolean[][] {
  return grid.map((row) => row.map((cell) => cell === 1));
}
